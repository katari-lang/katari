-- | @katari.lock@ — the pinned resolution of every reachable dependency.
--
-- @
-- [lock]
-- version  = 1                    # lockfile format version
-- snapshot = "v0.1.0"             # mirrored from katari.toml (omitted for path/git-only projects)
--
-- [packages.list_utils]
-- source = "git"
-- url    = "https://github.com/.../list_utils"
-- rev    = "v0.2.1"               # resolved full SHA
-- sha256 = "abc..."               # verified tarball content hash
--
-- [packages.local_fork]
-- source = "path"
-- path   = "../local_fork"        # no sha256: path deps are mutable on purpose
-- @
--
-- The lockfile is generated by @katari apply@ / @katari resolve@ and is meant to be committed so
-- every consumer gets the same byte-for-byte resolution.
--
-- A resolved non-path dependency is just a git ref plus its verified content hash, whether it came
-- from an explicit @[overrides.\<name>] git@ entry or from the registry snapshot — both are recorded
-- as a single @"git"@ source ('GitSource'). The /set/ a snapshot pins (top-level @snapshot = ...@)
-- is the only place "this came from the registry" lives; per-package, the two are identical and a
-- re-resolve reads @katari.toml@, not the lock, to decide where each comes from.
module Katari.Project.Lockfile
  ( Lockfile (..),
    LockedSource (..),
    PathLock (..),
    GitSource (..),
    lockfileFilename,
    lockfileFormatVersion,
    parseLockfile,
    renderLockfile,
    loadLockfile,
    writeLockfile,
  )
where

import Data.Char (isAsciiLower, isAsciiUpper, isDigit)
import Data.Map.Strict (Map)
import Data.Map.Strict qualified as Map
import Data.Maybe (fromMaybe)
import Data.Text (Text)
import Data.Text qualified as Text
import Data.Text.IO qualified as TextIO
import GHC.List (List)
import Katari.Project.Error
  ( FileErrorInfo (..),
    ProjectError (..),
    readFileOrError,
  )
import TOML
  ( DecodeTOML (..),
    decodeWith,
    getField,
    getFieldOpt,
    getFields,
    getFieldsOpt,
    renderTOMLError,
  )

-- | Conventional filename, sibling to @katari.toml@.
lockfileFilename :: FilePath
lockfileFilename = "katari.lock"

-- | Current lockfile schema version. Bumped only when the on-disk format changes incompatibly.
lockfileFormatVersion :: Int
lockfileFormatVersion = 1

data Lockfile = Lockfile
  { -- | Schema version (see 'lockfileFormatVersion').
    version :: Int,
    -- | The snapshot id in effect when this lock was generated; 'Nothing' for path/git-only deps.
    snapshot :: Maybe Text,
    -- | Resolved sources keyed by dep name; serialised alphabetically for deterministic contents.
    packages :: Map Text LockedSource
  }
  deriving (Show, Eq)

-- | Where a dependency comes from: a local path, or a content-addressed git ref.
data LockedSource
  = LockedPath PathLock
  | LockedGit GitSource
  deriving (Show, Eq)

-- | A path dependency: mutable on purpose, so no content hash is stored.
newtype PathLock = PathLock
  { location :: FilePath
  }
  deriving (Show, Eq)

-- | A pinned git source: a repo, the resolved full-SHA ref, and the verified tarball content hash.
-- Shared with the registry snapshot (see "Katari.Project.Snapshot"), since a snapshot is an
-- enumeration of exactly these.
data GitSource = GitSource
  { url :: Text,
    rev :: Text,
    sha :: Text
  }
  deriving (Show, Eq)

-- ===========================================================================
-- TOML vocabulary (one home, so parse and render cannot drift)
-- ===========================================================================

tableLock, tablePackages :: Text
tableLock = "lock"
tablePackages = "packages"

keyVersion, keySnapshot, keySource, keyUrl, keyRev, keySha256, keyPath :: Text
keyVersion = "version"
keySnapshot = "snapshot"
keySource = "source"
keyUrl = "url"
keyRev = "rev"
keySha256 = "sha256"
keyPath = "path"

tagPath, tagGit :: Text
tagPath = "path"
tagGit = "git"

-- ===========================================================================
-- Raw (pre-validation) decode target
-- ===========================================================================

-- | The @[packages.\<name>]@ table as decoded, before the per-@source@ field requirements are
-- checked. Keeping the fields optional lets a missing @sha256@ on a git pin be reported as a
-- 'LockfileValidationError' we phrase, not a generic decode error.
data RawLockedPackage = RawLockedPackage
  { source :: Text,
    url :: Maybe Text,
    rev :: Maybe Text,
    sha256 :: Maybe Text,
    path :: Maybe Text
  }

data RawLockfile = RawLockfile
  { version :: Int,
    snapshot :: Maybe Text,
    packages :: Map Text RawLockedPackage
  }

instance DecodeTOML RawLockedPackage where
  tomlDecoder =
    RawLockedPackage
      <$> getField keySource
      <*> getFieldOpt keyUrl
      <*> getFieldOpt keyRev
      <*> getFieldOpt keySha256
      <*> getFieldOpt keyPath

instance DecodeTOML RawLockfile where
  tomlDecoder =
    RawLockfile
      <$> getFields [tableLock, keyVersion]
      <*> getFieldsOpt [tableLock, keySnapshot]
      <*> (fromMaybe Map.empty <$> getFieldOpt tablePackages)

-- ===========================================================================
-- Parsing
-- ===========================================================================

-- | Parse the textual contents of @katari.lock@.
parseLockfile :: FilePath -> Text -> Either ProjectError Lockfile
parseLockfile path text = case decodeWith tomlDecoder text of
  Left tomlError ->
    Left (LockfileParseError FileErrorInfo {path = path, message = renderTOMLError tomlError})
  Right (raw :: RawLockfile) -> do
    validatedPackages <- traverse (validateLockedPackage path) (Map.toList raw.packages)
    pure Lockfile {version = raw.version, snapshot = raw.snapshot, packages = Map.fromList validatedPackages}

-- | Turn one decoded @[packages.\<name>]@ table into a 'LockedSource', enforcing the fields each
-- @source@ variant requires.
validateLockedPackage :: FilePath -> (Text, RawLockedPackage) -> Either ProjectError (Text, LockedSource)
validateLockedPackage path (name, raw)
  | raw.source == tagPath = do
      location <- require keyPath raw.path
      pure (name, LockedPath PathLock {location = Text.unpack location})
  | raw.source == tagGit = do
      gitSource <- GitSource <$> require keyUrl raw.url <*> require keyRev raw.rev <*> require keySha256 raw.sha256
      pure (name, LockedGit gitSource)
  | otherwise = validationError ("package '" <> name <> "' has unknown source '" <> raw.source <> "'")
  where
    require field =
      maybe (validationError ("package '" <> name <> "' is missing required field '" <> field <> "'")) Right
    validationError message = Left (LockfileValidationError FileErrorInfo {path = path, message = message})

-- ===========================================================================
-- Rendering
-- ===========================================================================

-- | Render a 'Lockfile' to deterministic, byte-stable TOML. Packages are emitted in ascending key
-- order (@Map.toAscList@) so the same 'Lockfile' always produces identical bytes; the result
-- round-trips through 'parseLockfile'.
renderLockfile :: Lockfile -> Text
renderLockfile lockfile =
  Text.unlines $
    [tableHeader tableLock, assign keyVersion (Text.pack (show lockfile.version))]
      <> maybe [] (\snapshotValue -> [assign keySnapshot (quote snapshotValue)]) lockfile.snapshot
      <> concatMap renderPackage (Map.toAscList lockfile.packages)

renderPackage :: (Text, LockedSource) -> List Text
renderPackage (name, lockedSource) =
  "" : tableHeader (tablePackages <> "." <> renderKey name) : renderSource lockedSource

renderSource :: LockedSource -> List Text
renderSource lockedSource = case lockedSource of
  LockedPath lock ->
    [assign keySource (quote tagPath), assign keyPath (quote (Text.pack lock.location))]
  LockedGit lock ->
    [ assign keySource (quote tagGit),
      assign keyUrl (quote lock.url),
      assign keyRev (quote lock.rev),
      assign keySha256 (quote lock.sha)
    ]

-- | A TOML @key = value@ line; @value@ is already rendered (quoted string or bare number).
assign :: Text -> Text -> Text
assign key value = key <> " = " <> value

-- | A TOML table header @[name]@.
tableHeader :: Text -> Text
tableHeader name = "[" <> name <> "]"

-- | A TOML key: bare when made of @[A-Za-z0-9_-]@, otherwise quoted.
renderKey :: Text -> Text
renderKey key
  | not (Text.null key) && Text.all isBareKeyChar key = key
  | otherwise = quote key
  where
    isBareKeyChar character =
      isAsciiLower character
        || isAsciiUpper character
        || isDigit character
        || character == '_'
        || character == '-'

-- | A TOML basic string literal: double-quoted, with backslash, quote, and control characters
-- escaped.
quote :: Text -> Text
quote value = "\"" <> Text.concatMap escapeChar value <> "\""
  where
    escapeChar character = case character of
      '\\' -> "\\\\"
      '"' -> "\\\""
      '\n' -> "\\n"
      '\r' -> "\\r"
      '\t' -> "\\t"
      _ -> Text.singleton character

-- ===========================================================================
-- IO
-- ===========================================================================

loadLockfile :: FilePath -> IO (Either ProjectError Lockfile)
loadLockfile path = do
  contents <- readFileOrError LockfileIOError path
  pure (contents >>= parseLockfile path)

-- | Write a 'Lockfile' to @path@, overwriting any existing file.
writeLockfile :: FilePath -> Lockfile -> IO ()
writeLockfile path lockfile = TextIO.writeFile path (renderLockfile lockfile)
