// StepCtx: the writable handle a thread op uses to mutate state and emit
// side-effects during a single internal-event dispatch.
//
// `state` is the live `State` object — ops mutate it directly. No
// Immer involvement: every tick creates a fresh `CoreModule` instance
// with state freshly loaded from the DB, so half-modified state on a
// thrown op is discarded with the instance (tx rollback handles the
// DB side).
//
// Outbound events / errors / logs are appended to plain arrays. The
// runner accumulates these across the queue drain and packs them into
// the final Result.
//
// Internal events generated by an op are pushed to the engine's queue;
// the runner picks them up on the next iteration of its drain loop.

import type { Event, InternalEventPayload } from "./event.js";
import type { LogEntry, LogLevel } from "./logger.js";
import type { State } from "./state.js";

export interface StepCtx {
  /** Live engine state for this step (mutated in place). */
  readonly state: State;
  /** Schedule another internal event to be processed in this applyEvent. */
  enqueue(event: InternalEventPayload): void;
  /** Push an outbound event (caller will forward externally). */
  emit(event: Event): void;
  /** Append a log entry. */
  log(level: LogLevel, message: string, context?: Record<string, unknown>): void;
}

/**
 * Bag of side-effect accumulators threaded through every step. The
 * runner allocates one per `applyEvent` invocation. Outbound events
 * and logs are collected here; the internal-event queue lives here
 * too, so an op pushing to `queue` schedules work for the next
 * iteration of the drain loop.
 */
export type StepBuffers = {
  queue: InternalEventPayload[];
  outbound: Event[];
  logs: LogEntry[];
};

export function emptyBuffers(): StepBuffers {
  return { queue: [], outbound: [], logs: [] };
}

/** Build a StepCtx that backs onto the given state + buffers. */
export function makeStepCtx(
  state: State,
  buffers: StepBuffers,
): StepCtx {
  return {
    state,
    enqueue(event) {
      buffers.queue.push(event);
    },
    emit(event) {
      buffers.outbound.push(event);
    },
    log(level, message, context) {
      buffers.logs.push({ level, message, context });
    },
  };
}
